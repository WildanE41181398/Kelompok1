package com.example.kelompok1.ui.notifications;

public class ModelMessages {

    private String id, user, subjek, body;

    public ModelMessages(){}

    public String getId(){ return id; }
    public void setId(String id){ this.id = id; }

    public String getUser(){ return user; }
    public void setUser(String user){ this.user = user; }

    public String getSubjek(){ return subjek; }
    public void setSubjek(String subjek){ this.subjek = subjek; }

    public String getBody(){ return body; }
    public void setBody(String body){ this.body = body; }
}
